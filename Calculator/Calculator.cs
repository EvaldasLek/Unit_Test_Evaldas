namespace Calculator
{
    internal class Calculator
    {
        static internal int Sum(int number1, int number2)
        {
            return number1 + number2;
        }
        static internal double Subtrack(int number1, int number2)
        {
            return number1 - number2;
        }
        static internal double Multiplay(int number1, int number2)
        {
            return number1 * number2;
        }

        static internal double Divide(int number1, int number2)
        { 
            return number1 / number2; 
        }



    }
}
